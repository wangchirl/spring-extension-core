public class CglibCalculator {


    public int add(int i, int j) {
        return i + j;
    }

    public int sub(int i, int j) {
        return i - j;
    }

    public int muti(int i, int j) {
        return i * j;
    }

    public int div(int i, int j) {
        return i / j;
    }
}
